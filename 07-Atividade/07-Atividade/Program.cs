float numero;
Console.WriteLine("Escreva um valor que direi se ele é ou não maior que 10.");
numero = int.Parse(Console.ReadLine());
if (numero > 10)
{
    Console.WriteLine("Seu número é maior que 10");
}
else
{
    Console.WriteLine("Seu número é menor que 10");
}
